public class SimpleCalculation {
    public static void main(String[] args){

        System.out.println("====================");
        System.out.println("       간단한 계산     ");
        System.out.println("====================");

        //숫자 출력
        System.out.println();
        System.out.println("===사칙연산 미리보기 ===");
        System.out.println("10 + 3 = " + (10 + 3));
        System.out.println("10 - 3 = " + (10 - 3));
        System.out.println("10 * 3 = " + (10 * 3));
        System.out.println("10 / 3 = " + (10 / 3));
        System.out.println("10 % 3 = " + (10 % 3));  //나머지

        //문자열 연결
        System.out.println();
        System.out.println("=== 문자열 연결 ===");
        System.out.println("Hello" + " " +"Java" + "1");
        System.out.println("1_2 = " + (1+2));
        System.out.println("결과:" + 100 + "점");
    }
}
